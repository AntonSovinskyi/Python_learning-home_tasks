# Full name greeting

first_name = "anton"
last_name = "sovinskyi"
print("Hello,", first_name.title() + ' ' + last_name.title() + "!", "Have a good day!")

# Another way of full name greeting

first_name = "anton"
last_name = "sovinskyi"
full_name = f"{first_name} {last_name}"
print(f"Hello, {full_name.title()}! Have a good day!")